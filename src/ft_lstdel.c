#include "../inc/fillit.h"

void	ft_lstdel(t_list **alst)
{
	if (*alst)
	{
		ft_lstdel(&((*alst)->next));
		ft_lstdel(&((*alst)->prev));
		ft_lstdelone(alst);
	}
}
